/* class01/ex29.c
 * 課題名：atoi_huffman_coding
 * 使用可能な関数：write, malloc, free
 * ------------------------------------------*
 * コマンドライン引数として文字列を受け取り、ハフマン符号化を行い、
 * 符号化されたビット列と各文字のコードを出力するプログラムを書きなさい。
 * 頻度表の作成、優先度付きキューの実装、ハフマン木の構築、コードの生成を行ってください。
 * 出力は各文字のコードと、元の文字列を符号化したビット列の2部構成とします。
 * 引数が1つでない場合、または無効な入力の場合は、"Error"と出力してください。
 *
 * 例：
 * $> ./huffman_coding "hello world"
 * Codes:
 * h: 000
 * e: 001
 * l: 01
 * o: 10
 *  : 110
 * w: 1110
 * r: 1111
 * d: 1101
 * Encoded: 000001010110111001011101110111101101
 * $> ./huffman_coding ""
 * Error
 */