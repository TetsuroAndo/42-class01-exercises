/* class01/ex27.c
 * 課題名：atoi_matrix_operations
 * 使用可能な関数：write, malloc, free
 * ------------------------------------------*
 * コマンドライン引数として2つの行列（文字列形式）と操作タイプを受け取り、
 * 指定された操作を行って結果を出力するプログラムを書きなさい。
 * 行列は "[[a,b],[c,d]]" の形式で表現され、操作タイプは "add"、"multiply"、"determinant" のいずれかとします。
 * 行列の要素は整数とし、結果も整数で出力してください。
 * 引数が3つでない場合、または無効な入力の場合は、"Error"と出力してください。
 *
 * 例：
 * $> ./matrix_operations "[[1,2],[3,4]]" "[[5,6],[7,8]]" "add"
 * [[6,8],[10,12]]
 * $> ./matrix_operations "[[1,2],[3,4]]" "[[5,6],[7,8]]" "multiply"
 * [[19,22],[43,50]]
 * $> ./matrix_operations "[[1,2],[3,4]]" "[[5,6],[7,8]]" "determinant"
 * -2
 * -4
 * $> ./matrix_operations "[[1,2],[3,4]]" "[[5,6],[7,8]]" "inverse"
 * Error
 */