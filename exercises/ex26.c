/* class01/ex26.c
 * 課題名：atoi_date_format_converter
 * 使用可能な関数：write, malloc, free
 * ------------------------------------------*
 * コマンドライン引数として日付文字列と、入力フォーマット、出力フォーマットを受け取り、
 * 指定されたフォーマットに従って日付を変換するプログラムを書きなさい。
 * フォーマット指定子は以下を使用します：
 * %Y: 4桁の年, %y: 2桁の年, %m: 2桁の月, %d: 2桁の日
 * %B: 月の完全名, %b: 月の短縮名
 * %A: 曜日の完全名, %a: 曜日の短縮名
 * 引数が3つでない場合、または無効な入力の場合は、"Error"と出力してください。
 *
 * 例：
 * $> ./date_converter "2023-05-15" "%Y-%m-%d" "%B %d, %Y (%A)"
 * May 15, 2023 (Monday)
 * $> ./date_converter "May 15, 2023" "%B %d, %Y" "%y/%m/%d"
 * 23/05/15
 * $> ./date_converter "2023-13-01" "%Y-%m-%d" "%B %d, %Y"
 * Error
 */