/* class01/ex25.c
 * 課題名：atoi_expression_evaluator
 * 使用可能な関数：write, malloc, free, atof, pow
 * ------------------------------------------*
 * コマンドライン引数として数学的表現を受け取り、その結果を計算して出力するプログラムを書きなさい。
 * サポートする演算子は +, -, *, /, ^（べき乗）, sqrt（平方根）とします。
 * 括弧 ( ) を使用した優先順位の変更もサポートしてください。
 * 結果は小数点以下6桁まで出力してください。
 * 引数が1つでない場合、または無効な表現の場合は、"Error"と出力してください。
 *
 * 例：
 * $> ./expression_evaluator "3 + 4 * 2 / ( 1 - 5 ) ^ 2"
 * 3.500000
 * $> ./expression_evaluator "sqrt(16) + 2^3"
 * 12.000000
 * $> ./expression_evaluator "2 + * 3"
 * Error
 */